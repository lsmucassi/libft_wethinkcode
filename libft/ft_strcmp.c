#include "libft.h"

int		ft_strcmp(const char *c1, const char *c2)
{
	int i;

	i = 0;
	while (c1[i] != '\0' && c2[i] != '\0' && c1[i] == c2[i])
		i++;
	return (c2[i] - c1[i]);
}
